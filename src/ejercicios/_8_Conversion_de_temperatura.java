package ejercicios;

public class _8_Conversion_de_temperatura {
        public static void main(String[] args) {
            double celsius = 25;
            double fahrenheit;
            fahrenheit = (celsius * 9/5) + 32;
            System.out.println("Equivalencia entre Celsius y fahrenheit ");
            System.out.println(celsius + " C° equivalen a " + fahrenheit + " F° ");
        }

}
